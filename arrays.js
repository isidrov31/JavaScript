// INDEX OF
let result = document.getElementById('buttonIndexOf');
result.addEventListener('click', buscarEnArray)

function buscarEnArray() {
    let input = document.getElementById('input').value;
    let arrayEjemplo = ['Messi', 'Depaul', 'Dibu'];
    let posicion = arrayEjemplo.indexOf(input);
    
    if (posicion !== -1) {
        document.getElementById('resultadoIndexOf').innerHTML = "La posicion del objeto en el array es: " + posicion;
    } else {
        document.getElementById('resultadoIndexOf').innerHTML = "El objeto no esta en el array";
    }
}

